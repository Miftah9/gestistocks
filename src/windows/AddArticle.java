    private void affichageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_affichageMouseClicked
        int selectedRow = affichage.getSelectedRow();
        if (selectedRow == -1) {
            return; // No row is selected, so do nothing.
        }

        article = articleList.get(selectedRow);
        nom_txt.setText(article.getNom());
        quantite_txt.setText(String.valueOf(article.getQuantite()));
        categorie_cbx.setSelectedItem(article.getCategorieId().getNom());
        gain_txt.setText(String.valueOf(article.getGain()));
    }//GEN-LAST:event_affichageMouseClicked

    private void supprimer_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimer_btnActionPerformed
        // TODO add your handling code here:
                int selectedRow = affichage.getSelectedRow();

        // Vérifier si une ligne a été sélectionnée
        if (selectedRow == -1) {
            return;
        }

        // Récupérer l'article à partir de la liste des articles
        Article a = articleList.get(selectedRow);

        // Supprimer l'article de la base de données
        IArticle ia = new ArticleService();
        ia.deleteArticle(a);

        // Supprimer l'article de la liste des articles
        articleList.remove(selectedRow);

        // Mettre à jour la liste des articles affichée dans la table
        affichage();

    }//GEN-LAST:event_supprimer_btnActionPerformed

    private void modifier_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifier_btnActionPerformed
        // TODO add your handling code here:
int selectedRow = affichage.getSelectedRow();

// Vérifier si une ligne a été sélectionnée
if (selectedRow == -1) {
    return;
}

// Récupérer les nouvelles valeurs des champs de texte pour l'article modifié
String nom = nom_txt.getText();
String quantite = quantite_txt.getText();




//Categorie c = (Categorie) categorie_cbx.getSelectedItem();

Categorie c = hms.get(categorie_cbx.getSelectedItem().toString());


 float gain = Float.parseFloat(gain_txt.getText()); // correction : parse le texte en float

// Mettre à jour les attributs de l'article avec les nouvelles valeurs
Article article = new Article();
article.setNom(nom);
article.setQuantite(Integer.parseInt(quantite));
article.setCategorieId(c);

article.setGain(gain);

// Mettre à jour l'article dans la base de données
IArticle articleService = new ArticleService();
articleService.updateArticle(article);

// Mettre à jour la liste des articles affichée dans la table
affichage();

    }//GEN-LAST:event_modifier_btnActionPerformed

