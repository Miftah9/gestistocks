    private void ajouter_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter_btnActionPerformed

             if (categorie_cbx.getSelectedIndex() == -1) {
             return;
             }
             String nom = nom_txt.getText();
             int quantite = Integer.parseInt(quantite_txt.getText()); // Conversion de String en int
             float gain = Float.parseFloat(gain_txt.getText());
             Categorie c = hm.get(categorie_cbx.getSelectedItem().toString());
             Article a = new Article(nom, quantite,gain, c);
             Article article = ia.addArticle(a);
             if (article.getId() == null) {
             JOptionPane.showMessageDialog(this, "Erreur lors de l'insertion");
             return;
             }
        clearFied();
        affichage();

    }//GEN-LAST:event_ajouter_btnActionPerformed
    private void clearFied(){
        nom_txt.setText("");
        quantite_txt.setText("");
        gain_txt.setText("");
        categorie_cbx.setSelectedIndex(-1);
        
    }
    
