    private void activer_ToggleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_activer_ToggleActionPerformed
        // TODO add your handling code here:
         // Récupérer l'utilisateur correspondant à ce bouton
    Utilisateur utilisateur = (Utilisateur) activer_Toggle.getClientProperty("utilisateur");

    // Inverser l'état d'activation de l'utilisateur
    utilisateur.setActivation(!utilisateur.getActivation());

    // Mettre à jour le texte et l'icône du bouton en fonction de l'état d'activation de l'utilisateur
    if (utilisateur.getActivation()) {
        activer_Toggle.setText("Activé");
    } else {
        activer_Toggle.setText("Désactivé");
    }

    // Mettre à jour l'utilisateur dans la base de données
    UtilisateurService utilisateurService = new UtilisateurService();
    utilisateurService.updateUtilisateur(utilisateur);
    }//GEN-LAST:event_activer_ToggleActionPerformed

