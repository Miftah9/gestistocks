    private void ajouter_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter_btnActionPerformed

        if (nom_txt.getText().isEmpty() || role_cbx.getSelectedIndex() == -1 || prenom_txt.getText().isEmpty() || login_txt.getText().isEmpty() || motdepasse_txt.getText().isEmpty()  ) {
        return;
    }


         String nom = nom_txt.getText();
         String prenom = prenom_txt.getText();
         String email = email_txt.getText();
         String login = login_txt.getText();
         String password = motdepasse_txt.getText();
         Role roleId = hm.get(role_cbx.getSelectedItem().toString());
      

    Utilisateur u = new Utilisateur(nom, prenom, email, login, password, roleId);
    IUtilisateur iu = new UtilisateurService(); // instanciation de l'interface
    Utilisateur utilisateur = iu.addUtilisateur(u);
    
    if (utilisateur.getId() == null) {
        JOptionPane.showMessageDialog(this, "Erreur lors de l'insertion");
        return;
    }

        clearFied();
        affichage();
    }//GEN-LAST:event_ajouter_btnActionPerformed
        private void clearFied() {
        nom_txt.setText("");
        prenom_txt.setText("");
        login_txt.setText("");
        motdepasse_txt.setText("");
        email_txt.setText("");
    }
        
    private void affichage() {
        
        UserList = iu.allUtilisateurs();
        
        List<String> elementOmit = new ArrayList<>();
        elementOmit.add("id"); //pour exclure l'id de l'affichage du tableau.      
        
        UtilsFonction.displayDataInTable(UserList, affichage, elementOmit);
    }
