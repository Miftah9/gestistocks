        private void loadFournisseur() {
    List<Fournisseur> fournisseurList = fi.allFournisseur();
    fournisseurList.stream().forEach(fournisseur -> {
        fournisseur_cbx.addItem(fournisseur.getTelephone());
        hm.put(fournisseur.getTelephone(), fournisseur);
    });
    fournisseur_cbx.setSelectedIndex(-1);
}

    private void loadArticle() {
    List<Article> articleList = iar.allArticles();
    articleList.stream().forEach(article -> {
        article_cbx.addItem(article.getNom());
        hms.put(article.getNom(), article);
    });
    article_cbx.setSelectedIndex(-1);
}




    


