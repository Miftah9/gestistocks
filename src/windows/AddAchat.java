    private void ajouter_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouter_btnActionPerformed

if (fournisseur_cbx.getSelectedIndex() == -1 || article_cbx.getSelectedIndex() == -1 ||  qte_achete_spr.getValue() == null) {
    JOptionPane.showMessageDialog(this, "Aucune champs doit etre vide");
   
    return;
}

double prix = Double.parseDouble(prix_txt.getText());
int quantiteAchetee = (int) qte_achete_spr.getValue();
Fournisseur fournisseurId = hm.get(fournisseur_cbx.getSelectedItem().toString());
Article articleId = hms.get(article_cbx.getSelectedItem().toString());
Date dateAchat = new Date();
Achat achat = new Achat(prix, quantiteAchetee, fournisseurId, articleId, dateAchat);

achat = ia.addAchat(achat);

if (achat.getId() == null) {
    JOptionPane.showMessageDialog(this, "Erreur lors de l'insertion");
    return;
}

clearFied();
affichage();

    }//GEN-LAST:event_ajouter_btnActionPerformed
        private void clearFied(){
        prix_txt.setText("");
        qte_achete_spr.getValue();
        fournisseur_cbx.setSelectedIndex(-1);
        article_cbx.setSelectedIndex(-1);
        
    }
